    private void buatLaporan(String bulan, String tahun){
        hapusPanel(mainPanel);
        mainPanel.add(laporanPanel);
        buatUlangPanel(mainPanel);
        DefaultTableModel bayar = (DefaultTableModel) laporanPengeluaranTable.getModel();
        DefaultTableModel masuk = (DefaultTableModel) laporanPendapatanTable.getModel();
        bayar.setRowCount(0);
        masuk.setRowCount(0);
        String kapan = bulan;
        kapan = kapan.concat(" - ");
        kapan = kapan.concat(tahun);
        bulanLaporan.setText(kapan);
        try{
            koneksi data = new koneksi();
            String sql = "SELECT SUM(total) AS sum FROM pembayaran WHERE bulan='" + bulan + "' AND status='LUNAS' AND tahun='" + tahun + "'";
            int keluar=0;
            data.rs = data.st.executeQuery(sql);
            if(data.rs.next())
                keluar = data.rs.getInt("sum");
            sql = "SELECT SUM(total) AS sum FROM pendapatan WHERE bulan='" + bulan + "' AND tahun='" + tahun + "'";
            int untungKotor=0;
            data.rs = data.st.executeQuery(sql);
            if(data.rs.next())
                untungKotor = data.rs.getInt("sum");
            laporanPengeluaranTable.setForeground(Color.red);
            
            sql = "SELECT * FROM pembayaran WHERE bulan='" + bulan + "' AND status='LUNAS' AND tahun='" + tahun + "'";
            data.rs = data.st.executeQuery(sql);
            while(data.rs.next()){
                Object[] row = {data.rs.getString("nama_bayar"), data.rs.getString("harga"), data.rs.getString("jumlah"), data.rs.getString("total")};
                bayar.addRow(row);
            }
            Object[] spasi = {"","","",""};
            Object[] rows = {"","","Total Pengeluaran",keluar};
            bayar.addRow(spasi);
            bayar.addRow(rows);
            
            laporanPendapatanTable.setForeground(Color.blue);
            sql = "SELECT * FROM pendapatan WHERE bulan='" + bulan + "' AND tahun='" + tahun + "'";
            data.rs = data.st.executeQuery(sql);
            while(data.rs.next()){
                Object[] row = {data.rs.getString("nama"), data.rs.getString("total"), 1, data.rs.getString("total")};  
                masuk.addRow(row);
            }
            Object[] rowsz = {"","","Total Pendapatan",untungKotor};
            masuk.addRow(spasi);
            masuk.addRow(rowsz);
            pendapatanKotor.setText(Integer.toString(untungKotor));
            pendapatanBersih.setText(Integer.toString(untungKotor-keluar));
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    

    private void cekPasswd(){
        try{
            koneksi data = new koneksi();
            String sql = "SELECT username, password FROM karyawan WHERE id='%s'";
            sql = String.format(sql, nip);
            data.rs = data.st.executeQuery(sql);
            if(data.rs.next()){
                if(data.rs.getString("username").equals(data.rs.getString("password"))){
                    warn cek = new warn(2);
                    cek.setVisible(true);
                }
            }
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }

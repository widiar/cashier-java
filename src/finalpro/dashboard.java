    private void cariPegawai(String nama){
        DefaultTableModel tabelCari = (DefaultTableModel) cariPegawaiTable.getModel();
        tabelCari.setRowCount(0);
        try{
            koneksi data = new koneksi();
            String sql = "SELECT karyawan.id, nama, alamat, jabatan.jabatan FROM karyawan JOIN jabatan ON jabatan.id = karyawan.jabatan" +
                            " WHERE nama LIKE '%" + nama +"%' AND karyawan.jabatan <> 1";
            data.rs = data.st.executeQuery(sql);
            while(data.rs.next()){
                String nip = data.rs.getString("id");
                String namaT  = data.rs.getString("nama");
                String alamat = data.rs.getString("alamat");
                String jb = data.rs.getString("jabatan");
                Object[] row = {nip , namaT, alamat, jb};
                tabelCari.addRow(row);
            }
            data.tutupKoneksi();
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
    

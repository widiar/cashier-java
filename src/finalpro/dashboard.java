    public void ambilProfileData(){
        try{
            koneksi data = new koneksi();
            String sql = "SELECT * FROM karyawan where id='%s'";
            sql = String.format(sql, nip);
            data.rs = data.st.executeQuery(sql);
            if(data.rs.next()){
                profileNIPTField.setText(data.rs.getString("id"));
                profileNamaTField.setText(data.rs.getString("nama"));
                profileEmailTField.setText(data.rs.getString("email"));
                profileAlamatTField.setText(data.rs.getString("alamat"));
                profileUsernameTField.setText(data.rs.getString("username"));
            }
            data.tutupKoneksi();
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }

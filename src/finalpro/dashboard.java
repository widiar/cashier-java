    private void tambahDataBarangButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahDataBarangButtonActionPerformed
        // TODO add your handling code here:
        String kode = kodeBarangTextField.getText();
        String nama = namaBarangTextField.getText();
        String jumlah = jumlahBarangTextField.getText();
        String nqty = qtyBarangTextField.getText();
        String hargaB = hargaBeliTextField.getText();
        String hargaJ = hargaJualTextField.getText();
        if(Integer.parseInt(hargaB)>Integer.parseInt(hargaJ))
            JOptionPane.showMessageDialog(null, "Harga Jual Lebih Kecil ANDA RUGI");
        else if(kode.isEmpty() || nama.isEmpty() || jumlah.isEmpty() || nqty.isEmpty() || hargaB.isEmpty() || hargaJ.isEmpty())
            JOptionPane.showMessageDialog(null, "ISIKAN SEMUA FIELD");
        else{
           String ok[] = tambahBarangTxt.getText().split(" ", 2);
           try{
                koneksi data = new koneksi();
                String sqlc = "SELECT kode, nama_brg FROM barang WHERE nama_brg='" + nama + "'";
                data.rs = data.st.executeQuery(sqlc);         
                if(data.rs.next() && data.rs.getString("nama_brg").equals(nama) && !(data.rs.getString("kode").equals(kode)))
                    JOptionPane.showMessageDialog(null, "Barang Sudah ada Silahkan Edit Barang");
                else{      
                    if(ok[0].equals("Tambah")){
                        String sql = "INSERT INTO barang (kode, nama_brg, stok, nama_qty, harga_beli, harga_jual) "
                        + "VALUES ('%s', '%s', %s, '%s', %s, %s)";
                        sql = String.format(sql, kode, nama, jumlah, nqty, hargaB, hargaJ);
                        data.st.execute(sql);
                        int tot = Integer.parseInt(jumlah) * Integer.parseInt(hargaB);
                        LocalDateTime lokal = LocalDateTime.now();
                        sql = "INSERT INTO pembayaran (nama_bayar, jumlah, harga, total, status, bulan, tahun) VALUES ('beli %s', %s, %s, %s, '%s', '%s', '%s')";
                        sql = String.format(sql, nama, jumlah, hargaB, tot, "LUNAS", lokal.getMonth(), lokal.getYear());
                        data.st.execute(sql);
                        JOptionPane.showMessageDialog(null, "Data Berhasil di Tambah");
                        kosongkanFieldBarang();
                    }else if(ok[0].equals("Edit")){
                        String hargaL = hargaB, stokL = jumlah;
                        String sql = "SELECT stok, harga_beli FROM barang WHERE kode = '" + kode + "'";
                        data.rs = data.st.executeQuery(sql);
                        if(data.rs.next()){
                            hargaL = data.rs.getString("harga_beli");
                            stokL = data.rs.getString("stok");
                        }
                        if(!hargaL.equals(hargaB) || !stokL.equals(jumlah)){
                            if(Integer.parseInt(jumlah) > Integer.parseInt(stokL)){
                                LocalDateTime lokal = LocalDateTime.now();
                                int jum = Integer.parseInt(jumlah) - Integer.parseInt(stokL);
                                int tot = jum * Integer.parseInt(hargaB);
                                sql = "INSERT INTO pembayaran (nama_bayar, jumlah, harga, total, status, bulan, tahun) VALUES ('beli %s', %s, %s, %s, '%s', '%s', '%s')";
                                sql = String.format(sql, nama, jum, hargaB, tot, "LUNAS", lokal.getMonth(), lokal.getYear());
                                data.st.execute(sql);
                            } 
                        }
                        sql = "UPDATE barang SET nama_brg='%s', stok=%s, nama_qty='%s', harga_beli=%s, harga_jual=%s WHERE kode='%s'";
                        sql = String.format(sql, nama, jumlah, nqty, hargaB, hargaJ, kode);
    //                    System.out.println(sql);
                        if(data.st.executeUpdate(sql)==1)
                            JOptionPane.showMessageDialog(null, "Data Berhasil di Ubah");
                    }
                }
                data.tutupKoneksi();
            }catch(Exception e){
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            tampilkanSemuaBarang();
        }
    }//GEN-LAST:event_tambahDataBarangButtonActionPerformed

